package jspbook.freebook;

public class FreeBook {
	
	//멤버변수 선언
	private int fb_id;
	private String fb_bookname;
	private String fb_writer;
	private String fb_publisher;
	private String fb_publicationdate;
	private String fb_memo;
	
	public String getFb_bookname() {
		return fb_bookname;
	}
	public void setFb_bookname(String fb_bookname) {
		this.fb_bookname = fb_bookname;
	}
	public String getFb_writer() {
		return fb_writer;
	}
	public void setFb_writer(String fb_writer) {
		this.fb_writer = fb_writer;
	}
	public String getFb_publisher() {
		return fb_publisher;
	}
	public void setFb_publisher(String fb_publisher) {
		this.fb_publisher = fb_publisher;
	}
	public String getFb_publicationdate() {
		return fb_publicationdate;
	}
	public void setFb_publicationdate(String fb_publicationdate) {
		this.fb_publicationdate = fb_publicationdate;
	}
	public String getFb_memo() {
		return fb_memo;
	}
	public void setFb_memo(String fb_memo) {
		this.fb_memo = fb_memo;
	}
	public int getFb_id() {
		return fb_id;
	}
	public void setFb_id(int fb_id) {
		this.fb_id = fb_id;
	}
	
	

}
